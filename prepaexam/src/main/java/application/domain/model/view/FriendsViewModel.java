package application.domain.model.view;

public class FriendsViewModel {
    private String id;
    private String name;

    public FriendsViewModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
